export type TTSCompactSpeakerAnalysisMode = 'presetVoice' | 'voiceDesign'

export function buildScenePlanningSystemPrompt(lang?: string): string {
    return joinPromptSections([
        promptSection('角色', [
            '你是多角色小说 TTS 的场景规划引擎。',
            languagePromptLine(lang),
        ]),
        promptSection('当前分支', [
            '场景规划阶段：只识别场景，并在每个场景内列出所属 block，不规划角色，不分配说话人。',
            '场景用于后续角色规划、匿名/路人角色推断、声音设计和未来场景效果音；当前不生成实际音频。',
        ]),
        promptSection('输入格式', [
            'blocks 是待规划文本；每个 block 有 b/t/l/x/m。',
            'knownSpeakers 是已有角色，可作为场景边界线索，但不要输出角色。',
        ]),
        promptSection('输出原则', [
            '只输出有稳定上下文价值的场景；不要为每个段落机械创建场景。',
            '场景 id 从 1 开始，按首次出现顺序递增。',
            '每个场景的 b 是 block id 数组；元素来自输入 blocks[].b，只列出能判断场景的 block，不确定的 block 可以省略。',
            '同一地点、同一时间、同一批人物关系和氛围连续时，应复用同一场景。',
        ]),
        promptSection('场景内容', [
            'n 用短场景名，例如 高中教室、医院诊室、街道路口、家中客厅。',
            'c 写默认人群画像，特别用于匿名/路人角色推断。例如 高中教室里多数发声者大概率是高中学生，医院诊室里可能是医生、病人或家属。',
            'q 写场景内声音设计约束，帮助后续区分一堆路人角色。例如 高中学生多为少年/少女音，班干部更清晰坚定，普通同学更随意、语速自然偏快。',
            'h 写场景边界和识别线索，例如 上课铃后进入教室、医生问诊开始、众人在路口等待红绿灯。',
            'fx 是未来效果音意图，可写 上课铃、教室嘈杂、街道车流；没有明确线索可省略。',
        ]),
        promptSection('禁止项', [
            '不要输出角色对白归属。',
            '不要把情绪变化很小的相邻段落拆成多个场景。',
            '不要凭空添加与文本矛盾的地点或人群。',
        ]),
    ])
}

export function buildSpeakerAnalysisSystemPrompt(lang: string | undefined, mode: TTSCompactSpeakerAnalysisMode): string {
    const modeSections = mode === 'presetVoice'
        ? [
            promptSection('当前分支', [
                '预设音色文本解析：代码已经把文本切成朗读原子，模型只负责为原子分配说话人、识别新说话人，并在有 voices 时为新说话人选择 v。',
                '新增重要或反复出现的说话人时，从 nextSpeakerId 开始分配正整数 id，并按首次出现顺序递增。',
                '如果提供 voices，为新增说话人按角色、性别、语言地区和人物气质选择 v。',
                '对白中的新说话人名称要使用从上下文能稳定推断出的角色名。',
            ]),
        ]
        : [
            promptSection('当前分支', [
                '当前是角色设计后的文本解析阶段。',
                '代码已经把文本切成朗读原子；模型只负责为原子分配 knownSpeakers 中的说话人。',
            ]),
        ]
    return joinPromptSections([
        promptSection('角色', [
            '你是多角色小说 TTS 的说话人分析引擎。',
            languagePromptLine(lang),
        ]),
        ...modeSections,
        promptSection('输入格式', [
            'blocks 使用紧凑字段；m=1 表示可能同时包含旁白和对白。',
            'blocks.u 是代码预切的朗读原子；u[].a 是原子 id，u[].x 是该原子的可朗读文本。',
            'u[].q=1 表示引号内文本；它可能是对白，也可能是强调、术语、标题、字样、心理引用或动作特指，必须先判断引号用途。',
            '纯标点、空白和无可读文字通常已由代码过滤；如果仍看到拟声词/环境声原子，用 k=s 标为效果音。',
            'knownSpeakers.h 是说话人识别线索/上下文。',
            'knownScenes 是场景上下文；blocks[].s 指向 knownScenes[].i。场景可用于判断匿名/路人角色的年龄、身份和场景内对话参与者。',
        ]),
        promptSection('归属目标', [
            '为每个需要发声的原子判断说话人。',
            'q=1 且确实是角色直接发言的原子才判断真实说话人；q=1 但只是强调/术语/标题/字样/招牌内容/心理引用/动作特指时使用 i=0。',
            '如果一个对白原子由多个角色同时喊出/齐声说出/异口同声发出，i 使用其中一个主要角色 id，并额外输出 is 数组列出所有共同发声角色 id。',
            '拟声词、动物叫声或环境声不是普通人声朗读内容，输出 k=s。',
            '输出 k=s 时，同时输出 fx 作为 ElevenLabs 音效生成 prompt；fx 用简洁英文描述实际声音、来源、强弱、空间环境和质感，不要写对白、旁白或解释。',
            '输出 k=s 时可输出 dur 表示音效秒数；短促撞击/动物叫声通常小于 2 秒，持续环境声可更长；拿不准就省略。',
            '紧跟对白的短发言归属只有在明确包含发声/说话动词或气息发声动作时才可输出 k=m 静音，例如 笑道/叹道/说道/问道/回答/低声说/开口/插话道/叹了口气。',
            '普通动作或反应不是 k=m，例如 注意到他的目光/一愣/点了点头/转移话题/眼中浮现/闭上嘴巴；这些应保持 i=0 旁白朗读。',
            '顿了顿/沉吟半晌/叹了口气这类短停顿或气息提示可以 k=m；有些抱歉地开口、半开玩笑地说道这类归属也可以 k=m。p 由你根据语境选择，不要机械套规则。',
            '如果短发言归属含明确表演信息，把 p 加到对应对白原子上。',
            '如果 p 的依据来自相邻的引号外短归属原子，必须同时给该归属原子输出 k=m,p；不要只给对白 p 却让归属原子作为旁白朗读。',
            'p 只写短标签，不写句子；优先使用 MiMo 推荐音频标签，如 平静、冷漠、俏皮、愧疚、紧张、疲惫、委屈、叹气、沉默片刻、轻笑、冷笑、哽咽、颤抖。',
            '只有置信度 <= 0.8 或说话人不确定时才输出 c。',
        ]),
        speakerAttributionPromptSection(),
        promptSection('禁止项', [
            '不要把无引号旁白归给角色；不要把强调、术语、标题、字样、招牌内容、心理引用或动作特指误当成对白。',
            '不要把混合旁白/对白的段落整段归给一个说话人。',
            '不要把姓名、发言归属短语、句首或句尾几个字从自然的旁白/对白范围中切开。',
            '不要积极吸收动作/反应；只有明确发声归属才静音并转成 p。',
            '不要把“某人笑道/叹道/叹了口气/低声说/冷冷道”这类短发声归属朗读出来；能明确对应前后对白时转成 p。',
            '不要给没有文本证据的对白添加夸张表演标签。',
        ]),
    ])
}

export function buildSpeakerPlanningSystemPrompt(lang?: string): string {
    return joinPromptSections([
        promptSection('角色', [
            '你是多角色小说 TTS 的角色规划和语音规划引擎。',
            languagePromptLine(lang),
        ]),
        promptSection('当前分支', [
            '角色规划阶段：只识别重要或反复发声角色，不进行文本分段。',
            '后续文本解析会使用你输出的 h 来保持说话人一致性。',
            '如果 blocks 中存在对白或发言动作，必须输出本批文本里的核心发声角色；只有确实没有角色对白时才允许返回空列表。',
        ]),
        promptSection('输入格式', [
            'blocks 是待规划文本；knownSpeakers 是已知角色。',
            'knownSpeakers.h 是已有的说话人识别线索/上下文。',
            'knownScenes 是场景上下文；blocks[].s 指向 knownScenes[].i。',
        ]),
        promptSection('输出原则', [
            '新增说话人从 nextSpeakerId 开始分配正整数 id，并按首次出现顺序递增。',
            '已在 knownSpeakers 中出现的说话人必须精确复用 i；除非需要纠正，否则不要重复返回。',
            '客户端会用 a/o/p/q 合成角色卡。',
            '如果角色主要出现在某些场景，输出 s 关联 scene id。',
        ]),
        promptSection('规划原则', [
            '优先规划具名角色、反复出现的角色、参与多轮对话的角色，以及影响后续说话人判断的无名角色。',
            '利用 knownScenes 推断无名/路人角色的默认属性。例如 高中教室里的“同学”“有人”“班长”通常是高中学生，除非对白透露为老师或其他身份。',
            '重点提取稳定人物属性，尤其是性别、年龄感、职业或社会身份、性格气质。',
            'q 是后续 voice design 的核心声音设计，必须覆盖：音色/质感、语速/节奏、音高/能量、口吻/表演。',
            'q 使用可执行的听觉描述：声线明暗/厚薄/颗粒感/气息感/鼻音/共鸣位置、语速快慢/停顿密度/句尾处理、音高/能量、情绪基调。',
            '核心角色即使原文描述不多，也要基于性别、年龄感、身份、性格和对白功能合理补足声音设计，让声音与其他核心角色明显不同。',
            'q 可以参考 MiMo 音色设计写法：例如“清亮偏低的少年音，质感干净微冷，语速偏慢，停顿克制，口吻平静疏离”；不要只写“年轻男性声线”。',
            '配角/路人的 q 可以较短，但也要避免多个男性或女性角色使用几乎相同的音色、语速和口吻。',
            'q 长度控制在 1-4 句，核心特征清楚优先；不要堆砌维度。',
            'q 避免互相冲突的要求，避免“普通的/正常的/有磁性/好听/像主角”这类空泛词，也不要写混响、回声、EQ、压缩等后期音质效果。',
            'q 不要写外貌、服装或剧情复述；只写声音和表演可执行信息。',
            '核心角色的 g 必须稳定；已知角色同名或同 id 时必须复用 knownSpeakers 的 g。中文男性姓名、上下文为少年/青年/他/医生等男性线索时应明确 g=1，禁止因默认音色或旁白混淆成女性。',
            'h 只用于后续文本分段识别说话人，不是声音风格。',
            'h 要写入别名、称呼、人物关系、常见发言动作、发言归属词、容易误判的直接称呼、场景范围和对话上下文。',
            'h 要提炼可复用规则，不要只复述一句当前原文。例：阿诺=名为阿诺的年轻人；“阿诺，...”多半是在称呼他而非他说话；阿诺开口/说道/叹气/回答时才强指向他是说话人。',
            '规划时特别总结：某角色被别人频繁称呼、某角色经常被代词谈论、某些引语前后有发言动作、某个场景里的核心对话参与者。',
        ]),
        promptSection('禁止项', [
            'a/o/p/q 不要塞入当前句子内容；使用短语，不要写长段落。',
            '未知细节可以保守推断或省略。',
            '除非一次性无名说话人影响对话连续性，否则不要纳入规划。',
            '不要包含旁白 i=0。',
        ]),
    ])
}

export function buildSpeakerAnalysisRepairPrompt(
    lang: string | undefined,
    mode: TTSCompactSpeakerAnalysisMode,
    customAnalysisPrompt?: string,
): string {
    const outputLines = mode === 'presetVoice'
        ? [
            'speakers 只返回纠错必需的新增或修正说话人；没有则返回空数组。',
        ]
        : [
            '沿用 knownSpeakers 中的说话人。',
        ]
    return joinPromptSections([
        promptSection('角色', [
            '你是多角色小说 TTS 的纠错分段引擎。',
            languagePromptLine(lang),
        ]),
        promptSection('当前分支', [
            '纠错文本解析阶段：代码已经筛出需要重判的异常 blocks，并已切成朗读原子。',
            '不要判断是否需要纠错；直接为传入 blocks 的原子重新输出正确说话人归属。',
        ]),
        promptSection('输入格式', [
            'blocks 是需要重判的异常文本；m=1 表示可能同时包含旁白和对白。',
            'knownSpeakers.h 是说话人识别线索/上下文。',
            'knownScenes 是场景上下文；blocks[].s 指向 knownScenes[].i。',
            'blocks.u 是代码预切的朗读原子。',
            'k=s 表示拟声词/效果音/动物叫声静音；k=m 表示只作为表演提示的发言归属短语静音；p 是短表演标签。',
            'k=s 的原子必须尽量输出 fx；fx 是给 ElevenLabs 的英文音效生成 prompt。dur 是可选音效秒数。',
        ]),
        promptSection('输出原则', [
            ...outputLines,
            '只返回传入 blocks 的原子说话人归属。',
        ]),
        promptSection('纠错重点', [
            '代码已经拆出旁白、动作、发言归属短语和引号内对白原子。',
            '引号后的发言归属短语（如 asked/said/replied 或 问道/说道/回答/叹道/开口）通常标识引号内对白的说话人；该归属短语本身仍然是旁白。',
            '如果发言归属短语只有“某人笑道/叹道/叹了口气/低声说/冷冷道”等明确发声/表演信息，可以对归属原子输出 k=m，并把明确表演信息用 p 输出到对应对白原子。',
            '普通动作或反应不要输出 k=m，例如 注意到他的目光/一愣/点了点头/转移话题/眼中浮现/闭上嘴巴；这些保持 i=0 旁白。',
            '顿了顿/沉吟半晌/叹了口气这类短停顿或气息提示可以输出 k=m；有些抱歉地开口、半开玩笑地说道这类归属也可以 k=m。p 由你根据语境选择，不要机械套规则。',
            '如果某个对白原子已经因相邻短归属原子获得 p，该短归属原子也必须输出 k=m,p；否则它会被旁白朗读，造成重复和断裂。',
            '拟声词、动物叫声或环境声原子输出 k=s，不参与普通语音合成。',
            '输出 k=s 时，同时输出 fx 作为 ElevenLabs 音效生成 prompt；fx 用简洁英文描述实际声音、来源、强弱、空间环境和质感，不要写对白、旁白或解释。',
            '输出 k=s 时可输出 dur 表示音效秒数；短促撞击/动物叫声通常小于 2 秒，持续环境声可更长；拿不准就省略。',
            '短引号内容未必是对白；如果它表示招牌字样、文本标题、概念强调、感知动作特指或被讨论的词本身，仍输出旁白 i=0。',
            '如果 q=1 的对白原子和引号外叙述/归属原子相邻，不要把它们归给同一个角色；引号外原子使用旁白。',
            '修正示例：“小明，你在看什么？”小红问道。=> 引号内对白说话人是小红；“小红问道”是旁白。',
            '修正示例：“他不是盲人。”阿诺说道，“他一定看得见。” => 两段引号都是阿诺；“阿诺说道，”是旁白。',
            '优化示例：“姨妈，补贴就是用来生活的。”林七夜笑道。=> 对白归林七夜并加 p=轻笑；“林七夜笑道”输出 k=m。',
        ]),
        speakerAttributionPromptSection(),
        promptSection('覆盖', [
            '为每个需要发声的原子输出归属；不要漏掉 q=1 的对白原子。',
            '不要合并或拆分原子；不要自造原子 id。',
        ]),
        customAnalysisPrompt ? promptSection('调用方补充约束', [customAnalysisPrompt]) : '',
        promptSection('禁止项', [
            '不要处理未传入的 block。',
            '不要把引号外叙述/动作/归属短语归给角色。',
        ]),
    ])
}

function joinPromptSections(sections: readonly string[]): string {
    return sections.filter(Boolean).join('\n\n')
}

function promptSection(title: string, lines: readonly (string | undefined)[]): string {
    return [`## ${title}`, ...lines.filter(Boolean)].join('\n')
}

function languagePromptLine(lang: string | undefined): string | undefined {
    return lang ? `语言提示：${lang}。` : undefined
}

